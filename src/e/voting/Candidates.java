    private void DeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteBtnMouseClicked
        // TODO add your handling code here:
        if(CdtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Select The Candidate To Be Deleted");
        } else {
            int ok = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete the data", "Confirmation", JOptionPane.YES_NO_CANCEL_OPTION);
            if(ok == 0) {
                try {
                    Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/evotedb", "root", "");
                    String Query = "DELETE FROM tb_candidates WHERE ID = '" + CdtID.getText() + "'";
                    Statement Del = Con.createStatement();
                    Del.executeUpdate(Query);
                    JOptionPane.showMessageDialog(this, "Candidate Deleted Successfully");
                    DisplayCandidates();
                    WidthColumn();
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(this, "Candidate Deleted Failed");
                }   
            }
        }
    }//GEN-LAST:event_DeleteBtnMouseClicked

    private void EditBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditBtnMouseClicked
        // TODO add your handling code here:
        if(imgpath != null) {
            try {
                try {
                    InputStream img = new FileInputStream(imgpath);
                    Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/evotedb", "root", "");
                    String Query = "UPDATE tb_candidates SET ID = ?, Name = ?, Age = ?, Gender = ?, Vote = ?, Vision = ?, Mission = ?, Photo = ? WHERE No = " + CId;
                    PreparedStatement Update = Con.prepareStatement(Query);
                    Update.setString(1, CdtID.getText());
                    Update.setString(2, CdtName.getText());
                    Update.setInt(3, Integer.valueOf(CdtAge.getText()));
                    Update.setString(4, CdtGen.getSelectedItem().toString());
                    Update.setString(5, CdtVote.getSelectedItem().toString());
                    Update.setString(6, CdtVisi.getText());
                    Update.setString(7, CdtMisi.getText());
                    Update.setBlob(8, img);
                    Update.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Candidate Update Successfully");
                    DisplayCandidates();  
                    WidthColumn();
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(this, "Candidate Update Failed");
                }
            } catch (HeadlessException | FileNotFoundException | NumberFormatException e) {
                JOptionPane.showMessageDialog(this, e);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Select Photo");
            CdtPict.setIcon(null);
            CdtPict.setText("");
        }
        
        imgpath = null;
    }//GEN-LAST:event_EditBtnMouseClicked


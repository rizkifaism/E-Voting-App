    private void BrowseBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BrowseBtnMouseClicked
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.Images", "jpg", "jpeg", "png");
        chooser.addChoosableFileFilter(filter);
        int result = chooser.showSaveDialog(null);
        if(result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = chooser.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            CdtPict.setIcon(ResizePhoto(path, null));
            imgpath = path;
        }
    }//GEN-LAST:event_BrowseBtnMouseClicked

    private void AddBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddBtnMouseClicked
        // TODO add your handling code here:
        if(CdtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter ID");
        } else if(CdtName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Name");
        } else if(CdtAge.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Age");
        } else if(CdtGen.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Please Enter Gender");
        } else if(CdtVote.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Please Enter Vote");
        } else if(CdtVisi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Vision");
        } else if(CdtMisi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Mission");
        } else {
            try {
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/evotedb", "root", "");
                PreparedStatement Add = Con.prepareStatement("INSERT INTO tb_candidates VALUES(?,?,?,?,?,?,?,?,?,?)");
                InputStream img = new FileInputStream(imgpath);
                CandidatesCount();
                Add.setInt(1, CId);
                Add.setString(2, CdtID.getText());
                Add.setString(3, CdtName.getText());
                Add.setInt(4, Integer.valueOf(CdtAge.getText()));
                Add.setString(5, CdtGen.getSelectedItem().toString());
                Add.setString(6, CdtVote.getSelectedItem().toString());
                Add.setString(7, CdtVisi.getText());
                Add.setString(8, CdtMisi.getText());
                Add.setBlob(9, img);
                Add.setInt(10, 0);
                int row = Add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Candidate Registered");
                Con.close();
                DisplayCandidates();
                WidthColumn();
            } catch (HeadlessException | FileNotFoundException | NumberFormatException | SQLException ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
        }
    }//GEN-LAST:event_AddBtnMouseClicked

    private ImageIcon ResizePhoto(String ImagePath, byte[] pic) {
        ImageIcon MyImage = null;
        
        if(ImagePath != null) {
            MyImage = new ImageIcon(ImagePath);
        } else {
            MyImage = new ImageIcon(pic);
        }
        
        Image img = MyImage.getImage();
        Image newImg = img.getScaledInstance(CdtPict.getWidth(), CdtPict.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        
        return image;
    }
    
    private void FetchPhoto() {
        String Query = "SELECT Photo FROM tb_candidates WHERE ID = '" + CdtID.getText() + "'";
        Statement St;
        ResultSet Rs;
        try {
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/evotedb", "root", "");
            St = Con.createStatement();
            Rs = St.executeQuery(Query);
            if(Rs.next()) {
                CdtPict.setIcon(ResizePhoto(null, Rs.getBytes("Photo")));
            }
        } catch (SQLException e) {
        }
    }
    
